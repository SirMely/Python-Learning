a = 10
b = 3

# Arithimitic Operators
'''
print(a + b)  # Addition → 13
print(a - b)  # Subtraction → 7
print(a * b)  # Multiplication → 30
print(a / b)  # Division → 3.3333
print(a // b)  # Floor division (removes decimal) → 3
print(a % b)  # Modulus (remainder) → 1
print(a ** b)  # Exponentiation (power) → 10^3 = 1000

'''
# Comparison Operators
'''
print(x == y)  # False
print(x != y)  # True
print(x > y)  # Greater than
print(x < y)  # Less than
print(x >= 5)  # Greater than or equal to
print(x <= 4)  # Less than or equal to
'''
# Logical Operators
'''
a = True
b = False

print(a and b)  # False (Both must be True)
print(a or b)  # True (At least one must be True)
print(not a)  # False (Reverses the value)
'''