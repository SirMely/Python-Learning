# If-Else Statements

age = 18

if age >= 18:
    print("You're an adult!")
else:
    print("You're a minor.")

# If-Elif Statements
score = 75

if score >= 90:
    print("Grade: A")
elif score >= 80:
    print("Grade: B")
elif score >= 70:
    print("Grade: C")
else:
    print("Grade: F") 

# Loops

# For loop
for i in range(5): # Loops from 0 to 4
    print("Iteration:", i)

# While loop
count = 0

while count < 3:
    print("Count:", count)
    count += 1 # Increase count by 1 each time